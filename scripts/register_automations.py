def _existing_auto_jobs() -> dict:
    return {
        j.get("name"): j
        for j in list_jobs()
        if str(j.get("name", "")).startswith(NAME_PREFIX)
    }


def cmd_remove() -> int:
    existing = _existing_auto_jobs()
    if not existing:
        print("삭제할 'auto:' job 없음.")
        return 0
    for name, job in existing.items():
        remove_job(job["id"])
        print(f"삭제됨: {name} (id={job['id']})")
    return 0


def main() -> int:
    parser = argparse.ArgumentParser(description="QJC Hermes 자동화 cron job 등록")
    parser.add_argument("--apply", action="store_true", help="실제 등록 (기본은 dry-run)")
    parser.add_argument("--force", action="store_true", help="기존 동일 name 삭제 후 재등록")
    parser.add_argument("--remove", action="store_true", help="'auto:' job 전체 삭제")
    args = parser.parse_args()

    if args.remove:
        return cmd_remove()

    # 근본 방어: cron job 의 skill 은 HERMES_HOME/skills/ 에서 로드된다(skills_tool.SKILLS_DIR).
    # 프로젝트 skills/ 에서만 만들고 sync 를 안 하면 런타임이 "Skill not found" 로 실패한다.
    # 등록 직전 표준 배포 도구로 sync 하여 스킬·cron·래퍼가 항상 함께 배포되도록 보장.
    if args.apply:
        from tools.skills_sync import sync_skills
        sr = sync_skills(quiet=True)
        print(f"스킬 배포(~/.hermes/skills/): {len(sr.get('copied', []))} new, "
              f"{len(sr.get('updated', []))} updated, "
              f"{len(sr.get('user_modified', []))} user-modified 보존\n")

    existing = _existing_auto_jobs()
    registered = skipped = 0

    for auto in AUTOMATIONS:
        name = auto["name"]
        exists = name in existing

        if exists and not args.force:
            print(f"SKIP (이미 존재): {name}")
            skipped += 1
            continue

        if not args.apply:
            tag = "재등록 예정" if exists else "등록 예정"
            print(f"[dry-run] {tag}: {name} | {auto['schedule']} | deliver={auto['deliver']}")
            registered += 1
            continue

        if exists and args.force:
            remove_job(existing[name]["id"])

        # cron 가드 통과용 래퍼를 HERMES_HOME/scripts/auto/ 에 생성하고 그 상대경로를 등록.
        script_ref = _make_wrapper(auto["skill"], auto["script"]) if auto.get("script") else None

        job = create_job(
            prompt=auto["prompt"],
            schedule=auto["schedule"],
            name=name,
            skill=auto["skill"],
            script=script_ref,
            deliver=auto["deliver"],
        )
        print(f"등록됨: {name} (id={job['id']}) | next={job.get('next_run_at')}")
        registered += 1

    mode = "등록" if args.apply else "dry-run"
    print(f"\n{mode} 완료: {registered}건 처리, {skipped}건 skip "
          f"(전체 {len(AUTOMATIONS)}개 자동화).")
    if not args.apply:
        print("실제 등록하려면 --apply 를 붙여 다시 실행하세요.")
        print("전송 대상: TELEGRAM_HOME_CHANNEL / DISCORD_HOME_CHANNEL 환경변수 설정 필요.")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
