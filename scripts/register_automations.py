def _script(rel: str) -> str:
    """프로젝트 루트 기준 수집 스크립트의 절대 경로."""
    return str(PROJECT_ROOT / rel)


def _make_wrapper(skill: str, target_abs: str) -> str:
    """~/.hermes/scripts/auto/{skill}.py 래퍼를 만들고 cron 기준 상대경로를 반환.

    cron(scheduler._run_job_script)은 script 가 HERMES_HOME/scripts/ 안에 있어야만
    실행한다 (path.resolve() 후 검사 — symlink 불가). 그래서 scripts 디렉토리 안에
    실제 래퍼 파일을 두고, runpy 로 프로젝트의 collect.py 를 실행한다.
    """
    auto_dir = get_hermes_home() / "scripts" / "auto"
    auto_dir.mkdir(parents=True, exist_ok=True)
    wrapper = auto_dir / f"{skill}.py"
    wrapper.write_text(
        "#!/usr/bin/env python3\n"
        "# AUTO-GENERATED by register_automations.py — 수정하지 마세요.\n"
        "# cron 가드(HERMES_HOME/scripts 제한)를 통과하기 위한 얇은 래퍼.\n"
        "import runpy\n"
        f"runpy.run_path({target_abs!r}, run_name='__main__')\n",
        encoding="utf-8",
    )
    try:
        os.chmod(wrapper, 0o700)
    except OSError:
        pass
    return f"auto/{skill}.py"  # HERMES_HOME/scripts/ 기준 상대경로


